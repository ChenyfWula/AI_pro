class graphAgent:
    def __init__(self,graph,weather,start,end,vilige,mine,DDL,MaxResource):
        self.graph = graph
        self.weather = weather
        self.start = start
        self.end = end
        self.vilige = vilige
        self.mine = mine
        self.DDL = DDL
        self.MaxResource = MaxResource
    
    def getWeather(self):
        return self.weather
    
    def getNeighbour(self, loc):
        if loc == self.getEnd():
            return None
        return self.graph[loc]
    
    def getStart(self):
        return self.start
    
    def getEnd(self):
        return self.end
    
    def is_mine(self, loc):
        return loc == self.mine
    
    def is_vilige(self, loc):
        return loc == self.vilige
    
    def getDDL(self):
        return self.DDL
    
        
